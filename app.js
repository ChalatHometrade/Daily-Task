function saveDailyLog(form) {
  const date = state.selectedDailyDate || getTodayKey();
  const completedTaskIds = [...form.querySelectorAll('input[name="task"]:checked')].map((input) => input.value);
  const note = new FormData(form).get("note") || "";
  const existing = getDailyLog(date);
  const payload = { completedTaskIds, note, savedAt: new Date().toISOString() };
  if (existing) Object.assign(existing, payload);
  else state.data.dailyLogs.push({ id: createId("dailyLog"), date, ...payload });
  saveUserDataChange();
  showToast(isToday(date) ? "บันทึกแล้ว วันนี้ทำได้ดีขึ้นอีกนิด" : "บันทึกย้อนหลังแล้ว");
  renderApp();
}

function getOrCreateDailyLog(dateKey) {
  let log = getDailyLog(dateKey);
  if (!log) {
    log = {
      id: createId("dailyLog"),
      date: dateKey,
      completedTaskIds: [],
      note: "",
      savedAt: new Date().toISOString()
    };
    state.data.dailyLogs.push(log);
  }
  if (!Array.isArray(log.completedTaskIds)) log.completedTaskIds = [];
  return log;
}

function saveDailyLogForDate(dateKey, completedTaskIds, note) {
  const log = getOrCreateDailyLog(dateKey);
  log.completedTaskIds = unique(completedTaskIds);
  log.note = note ?? log.note ?? "";
  log.savedAt = new Date().toISOString();
  saveUserDataChange();
  return log;
}

function completeDailyTask(taskId) {
  const dateKey = state.selectedDailyDate || getTodayKey();
  const log = getOrCreateDailyLog(dateKey);
  if (!log.completedTaskIds.includes(taskId)) log.completedTaskIds.push(taskId);
  saveDailyLogForDate(dateKey, log.completedTaskIds, log.note || "");
  showToast(isToday(dateKey) ? "บันทึกแล้ว" : "บันทึกย้อนหลังแล้ว");
  renderApp();
}

