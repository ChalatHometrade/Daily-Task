function renderCalendarDay(day) {
  const level = !day.hasLog ? "no-log" : day.percent < 40 ? "low" : day.percent < 70 ? "mid" : "high";
  return `
    <button class="calendar-day ${level} ${isToday(day.date) ? "today" : ""} ${day.date === state.selectedDailyDate ? "selected" : ""}" data-open-date="${day.date}" title="${day.percent}%">
      <span>${Number(day.date.slice(-2))}</span>
      <strong>${day.hasLog ? `${day.percent}%` : "-"}</strong>
    </button>
  `;
}

