function normalizeData(input) {
  const empty = createEmptyData();
  const moodLogs = Array.isArray(input?.moodLogs) ? input.moodLogs.map((log) => {
    const triggers = getMoodTriggers(log);
    return {
      ...log,
      trigger: log.trigger || formatTriggers(triggers),
      triggers,
      note: log.note || ""
    };
  }) : [];
  const oneTimeTasks = Array.isArray(input?.oneTimeTasks) ? input.oneTimeTasks.map((task) => ({
    ...task,
    priority: task.priority || "สำคัญปานกลาง"
  })) : [];
  const data = {
    ...empty,
    ...input,
    version: VERSION,
    dailyTasks: Array.isArray(input?.dailyTasks) ? input.dailyTasks : [],
    dailyLogs: Array.isArray(input?.dailyLogs) ? input.dailyLogs : [],
    oneTimeTasks,
    moodLogs,
    categories: Array.isArray(input?.categories) && input.categories.length ? input.categories : [...DEFAULT_CATEGORIES],
    settings: {
      ...empty.settings,
      ...(input?.settings || {}),
      sync: { ...DEFAULT_SYNC_SETTINGS, ...(input?.settings?.sync || {}) }
    }
  };
  const sync = data.settings.sync;
  if (!sync.defaultSyncConfigured) {
    sync.webAppUrl = sync.webAppUrl || DEFAULT_WEB_APP_URL;
    sync.enabled = true;
    sync.autoSyncEnabled = true;
    sync.autoSyncIntervalSeconds = sync.autoSyncIntervalSeconds || 60;
    sync.defaultSyncConfigured = true;
  }
  return data;
}

