function calculateDailyDateStats(dateKey) {
  const tasks = getActiveDailyTasks();
  const log = getDailyLog(dateKey);
  const completedSet = new Set(log?.completedTaskIds || []);
  const completed = tasks.filter((task) => completedSet.has(task.id)).length;
  const total = tasks.length;
  return { date: dateKey, completed, total, percent: total ? Math.round((completed / total) * 100) : 0, hasLog: !!log };
}

function calculateProgressStats(monthKey) {
  const days = Array.from({ length: getDaysInMonth(monthKey) }, (_, index) => calculateDailyDateStats(getDateFromMonthDay(monthKey, index + 1)));
  const loggedDays = logsForMonth(monthKey).length;
  const missedDateKeys = getMissedDailyLogDates(monthKey);
  const categoryStats = calculateCategoryProgress(monthKey);
  const taskStatsAll = calculateTaskProgress(monthKey);
  const top = taskStatsAll.slice().sort((a, b) => b.percent - a.percent || b.completed - a.completed).slice(0, 5);
  const bottom = taskStatsAll.slice().sort((a, b) => a.percent - b.percent || a.completed - b.completed).slice(0, 5);
  const loggedPossible = loggedDays * getActiveDailyTasks().length;
  const completed = logsForMonth(monthKey).reduce((sum, log) => sum + (Array.isArray(log.completedTaskIds) ? log.completedTaskIds.filter((id) => getActiveDailyTasks().some((task) => task.id === id)).length : 0), 0);
  const categoriesWithTasks = categoryStats.filter((item) => item.possible > 0);
  return {
    monthKey,
    days,
    loggedDays,
    missedDays: missedDateKeys.length,
    completionRate: loggedPossible ? Math.round((completed / loggedPossible) * 100) : 0,
    bestCategory: categoriesWithTasks.slice().sort((a, b) => b.percent - a.percent)[0]?.category || "",
    weakestCategory: categoriesWithTasks.slice().sort((a, b) => a.percent - b.percent)[0]?.category || "",
    bestTask: top[0]?.name || "",
    mostMissedTask: bottom[0]?.name || "",
    categoryStats,
    taskStats: { all: taskStatsAll, top, bottom },
    missedDateKeys
  };
}

function calculateCategoryProgress(monthKey) {
  const logs = logsForMonth(monthKey);
  const tasks = getActiveDailyTasks();
  return state.data.categories.map((category) => {
    const categoryTasks = tasks.filter((task) => task.category === category);
    const possible = categoryTasks.length * logs.length;
    const completed = logs.reduce((sum, log) => {
      const set = new Set(log.completedTaskIds || []);
      return sum + categoryTasks.filter((task) => set.has(task.id)).length;
    }, 0);
    return { category, completed, possible, percent: possible ? Math.round((completed / possible) * 100) : 0 };
  }).filter((item) => item.possible > 0 || tasks.some((task) => task.category === item.category));
}

function calculateTaskProgress(monthKey) {
  const logs = logsForMonth(monthKey);
  return getActiveDailyTasks().map((task) => {
    const completed = logs.filter((log) => (log.completedTaskIds || []).includes(task.id)).length;
    return {
      id: task.id,
      name: task.name,
      category: task.category,
      completed,
      loggedDays: logs.length,
      percent: logs.length ? Math.round((completed / logs.length) * 100) : 0
    };
  });
}

function calculateTaskCompletionByFullMonth(monthKey) {
  const logs = logsForMonth(monthKey);
  const possible = getDaysInMonth(monthKey);
  return getActiveDailyTasks().map((task) => {
    const completed = logs.filter((log) => (log.completedTaskIds || []).includes(task.id)).length;
    return {
      id: task.id,
      name: task.name,
      category: task.category,
      completed,
      possible,
      percent: possible ? Math.round((completed / possible) * 100) : 0
    };
  }).sort((a, b) => a.percent - b.percent || a.name.localeCompare(b.name));
}

function calculateMoodSummary(monthKey) {
  const possible = getDaysInMonth(monthKey);
  const counts = getMoodLogsByDateForMonth(monthKey).reduce((map, log) => {
    if (!log.mainEmotion) return map;
    map.set(log.mainEmotion, (map.get(log.mainEmotion) || 0) + 1);
    return map;
  }, new Map());
  return Array.from(counts.entries()).map(([label, count]) => ({
    label,
    count,
    possible,
    percent: possible ? Math.round((count / possible) * 100) : 0
  })).sort((a, b) => b.count - a.count || a.label.localeCompare(b.label));
}

function calculateTriggerSummary(monthKey) {
  const possible = getDaysInMonth(monthKey);
  const byDate = state.data.moodLogs
    .filter((log) => log.date?.startsWith(monthKey))
    .reduce((map, log) => {
      if (!map.has(log.date)) map.set(log.date, new Set());
      getMoodTriggers(log).filter(Boolean).forEach((trigger) => map.get(log.date).add(trigger));
      return map;
    }, new Map());
  const counts = Array.from(byDate.values()).reduce((map, triggers) => {
    triggers.forEach((trigger) => map.set(trigger, (map.get(trigger) || 0) + 1));
    return map;
  }, new Map());
  return Array.from(counts.entries()).map(([label, count]) => ({
    label,
    count,
    possible,
    percent: possible ? Math.round((count / possible) * 100) : 0
  })).sort((a, b) => b.count - a.count || a.label.localeCompare(b.label));
}

function getMissedDailyLogDates(monthKey) {
  const today = getTodayKey();
  const endDay = monthKey === getMonthKey(new Date()) ? Number(today.slice(-2)) : monthKey < getMonthKey(new Date()) ? getDaysInMonth(monthKey) : 0;
  const logged = new Set(logsForMonth(monthKey).map((log) => log.date));
  return Array.from({ length: endDay }, (_, index) => getDateFromMonthDay(monthKey, index + 1)).filter((date) => !logged.has(date));
}

