function getMoodLogsByDateForMonth(monthKey) {
  const byDate = state.data.moodLogs
    .filter((log) => log.date?.startsWith(monthKey))
    .reduce((map, log) => {
      const current = map.get(log.date);
      if (!current || getMoodSortTime(log) > getMoodSortTime(current)) map.set(log.date, log);
      return map;
    }, new Map());
  return Array.from(byDate.values());
}

function getMoodSortTime(log) {
  return log?.updatedAt || log?.createdAt || `${log?.date || ""}T${log?.time || "00:00"}`;
}

function getMoodTriggers(log) {
  if (!log) return ["ไม่มี"];
  if (Array.isArray(log.triggers) && log.triggers.length) return unique(log.triggers.map((item) => String(item).trim()).filter(Boolean));
  if (typeof log.trigger === "string" && log.trigger.trim()) {
    return unique(log.trigger.split(",").map((item) => item.trim()).filter(Boolean));
  }
  return ["ไม่มี"];
}

function getSelectedTriggers() {
  const selected = Array.isArray(state.selectedTriggers) ? state.selectedTriggers : [state.selectedTrigger || "ไม่มี"];
  const clean = unique(selected.map((item) => String(item).trim()).filter(Boolean));
  return clean.length ? clean : ["ไม่มี"];
}

function formatTriggers(triggers) {
  const clean = unique((Array.isArray(triggers) ? triggers : [triggers]).map((item) => String(item).trim()).filter(Boolean));
  return clean.length ? clean.join(", ") : "ไม่มี";
}

function toggleSelectedTrigger(trigger) {
  if (trigger === "ไม่มี") {
    state.selectedTriggers = ["ไม่มี"];
  } else {
    const current = getSelectedTriggers().filter((item) => item !== "ไม่มี");
    state.selectedTriggers = current.includes(trigger) ? current.filter((item) => item !== trigger) : [...current, trigger];
    if (!state.selectedTriggers.length) state.selectedTriggers = ["ไม่มี"];
  }
  state.selectedTrigger = formatTriggers(state.selectedTriggers);
}

